package deps

import (
	"fmt"
	"os"
	"sort"
	"strings"

	"github.com/BurntSushi/toml"
)

const LockFile = "yar.lock"

// LockEntry represents a single locked dependency in yar.lock.
type LockEntry struct {
	Name   string `toml:"name"`
	Git    string `toml:"git"`
	Tag    string `toml:"tag,omitempty"`
	Rev    string `toml:"rev,omitempty"`
	Branch string `toml:"branch,omitempty"`
	Commit string `toml:"commit"`
	Hash   string `toml:"hash"`
}

// lockFile is the raw TOML structure for yar.lock.
type lockFile struct {
	Package []LockEntry `toml:"package"`
}

// ReadLockFile parses a yar.lock file at the given path.
func ReadLockFile(path string) ([]LockEntry, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, fmt.Errorf("reading %s: %w", path, err)
	}
	return ParseLockFile(string(data))
}

// ParseLockFile parses yar.lock content from a string.
func ParseLockFile(content string) ([]LockEntry, error) {
	var raw lockFile
	if _, err := toml.Decode(content, &raw); err != nil {
		return nil, fmt.Errorf("parsing yar.lock: %w", err)
	}
	for i, entry := range raw.Package {
		if entry.Name == "" {
			return nil, fmt.Errorf("yar.lock: package[%d] missing name", i)
		}
		if entry.Git == "" {
			return nil, fmt.Errorf("yar.lock: package %q missing git", entry.Name)
		}
		if entry.Commit == "" {
			return nil, fmt.Errorf("yar.lock: package %q missing commit", entry.Name)
		}
		if entry.Hash == "" {
			return nil, fmt.Errorf("yar.lock: package %q missing hash", entry.Name)
		}
	}
	return raw.Package, nil
}

// WriteLockFile serializes lock entries to yar.lock format.
func WriteLockFile(entries []LockEntry) string {
	sorted := make([]LockEntry, len(entries))
	copy(sorted, entries)
	sort.Slice(sorted, func(i, j int) bool {
		return sorted[i].Name < sorted[j].Name
	})

	var b strings.Builder
	b.WriteString("# Auto-generated by yar. Do not edit.\n\n")
	for i, entry := range sorted {
		if i > 0 {
			b.WriteString("\n")
		}
		b.WriteString("[[package]]\n")
		fmt.Fprintf(&b, "name = %q\n", entry.Name)
		fmt.Fprintf(&b, "git = %q\n", entry.Git)
		if entry.Tag != "" {
			fmt.Fprintf(&b, "tag = %q\n", entry.Tag)
		}
		if entry.Rev != "" {
			fmt.Fprintf(&b, "rev = %q\n", entry.Rev)
		}
		if entry.Branch != "" {
			fmt.Fprintf(&b, "branch = %q\n", entry.Branch)
		}
		fmt.Fprintf(&b, "commit = %q\n", entry.Commit)
		fmt.Fprintf(&b, "hash = %q\n", entry.Hash)
	}
	return b.String()
}

// LockIndex builds a map from dependency name to LockEntry for fast lookup.
func LockIndex(entries []LockEntry) map[string]*LockEntry {
	m := make(map[string]*LockEntry, len(entries))
	for i := range entries {
		m[entries[i].Name] = &entries[i]
	}
	return m
}
